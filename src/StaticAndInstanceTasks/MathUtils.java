package StaticAndInstanceTasks;

public class MathUtils {
//    Tapşırıq class adı: MathUtilsTask
//    MathUtils adlı bir class yaradın. İçində iki static metod yazın:
//            • square(int number): ədədin kvadratını qaytarsın
//• cube(int number): ədədin kubunu qaytarsın
//    Tələblər:
//            • Metodları birbaşa MathUtils.square(5) və MathUtils.cube(3) şəklində çağırın
//• Nəticələri ekrana çap edin
    public static int square(int number){
        return number*number;
    }
    public static int cube(int number){
        return number*number*number;
    }

}
