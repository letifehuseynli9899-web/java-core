package JavaBasicsTasks;

public class VariableTypes {

    public static void main(String[] args) {
        int a = 4;
        float b = 0.5F;
        double c = 3.4;
        char d = 'A';
        boolean k = true;
        System.out.println(k);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
